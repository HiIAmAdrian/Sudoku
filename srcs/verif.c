#include "punct.h"

int	verif(char **argv)
{
	int i;
	int j;
	int k;

	i = 0;
	k = 0;
	while (i < 9)
	{
		j = 0;
		while (j < 9)
		{
			if ((argv[i][j] >= '1' && argv[i][j] <= '9') || argv[i][j] == '.')
				k = 1;
			else
				k = 0;
			if (k == 0)
				return (0);
			j++;
		}
		i++;
	}
	return (1);
}
